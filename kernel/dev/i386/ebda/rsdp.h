#ifndef _RSDP_H
#define _RSDP_H

#include <types.h>

struct rsdp_descriptor {
    uint8_t signature[8];
    uint8_t checksum;
    uint8_t oem_id[6];
    uint8_t revision;
    uint32_t rsdt_address;
} __attribute__((packed));

struct rsdp_descriptor_2 {
    struct rsdp_descriptor firstPart;

    uint32_t length;
    uint64_t xsdt_address;
    uint8_t extended_checksum;
    uint8_t reserved[3];
} __attribute__((packed));

#endif